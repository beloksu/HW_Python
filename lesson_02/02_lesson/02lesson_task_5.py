def month_to_season(month):
    if month == 1:
        return "Январь"
    elif month == 2:
        return "Февраль"
    elif month == 3:
        return "Март"
    elif month == 4:
        return "Апрель"
    elif month == 5:
        return "Май"
    elif month == 6:
        return "Июнь"
    elif month == 7:
        return "Июль"
    elif month == 8:
        return "Август"
    elif month == 9:
        return "Сентябрь"
    elif month == 10:
        return "Октябрь"
    elif month == 11:
        return "Ноябрь"
    elif month == 12:
        return "Декабрь"
    else:
        return "Неверный номер месяца"
